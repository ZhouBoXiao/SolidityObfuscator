BLACK = "\033[0m"
RED = "\033[1;31m"
GREEN = "\033[1;32m"
YELLOW = "\033[1;33m"
BLUE = "\033[1;34m"
PURPLE = "\033[1;35m"
AQUA = "\033[1;36m"

red=lambda x:RED+str(x)+BLACK
green=lambda x:GREEN+str(x)+BLACK
yellow=lambda x:YELLOW+str(x)+BLACK 
blue=lambda x:BLUE+str(x)+BLACK 
purple=lambda x:PURPLE+str(x)+BLACK 
aqua=lambda x:AQUA+str(x)+BLACK
